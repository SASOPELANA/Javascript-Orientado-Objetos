// objetos en javascript

// Tipo de datos primitivos en javascript
// Culquier tipo de datos que no sea un objeto es un dato primitivo

console.log(typeof "Hola mundo");
console.log(typeof 100);
console.log(typeof false);

// Obejectos en javascript

console.log(typeof {});
console.log(typeof []);

console.log(typeof new Date());
console.log(typeof new RegExp());

// con la palabra new podemos crear objetos e equivalentes al tipo de datos primitivos, con palabras reservadas Number, String, Boolean
console.log(new Number(100));
console.log(new String("Hola Mundo"));
console.log(new Boolean(true));
